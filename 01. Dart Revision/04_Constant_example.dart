// Program for Constant

void main()
{
  const int myConstInt = 10; // with data type
  const myConstDouble = 3.14; // without data type
  const String myConstString = 'Hello, Dart!'; // with data type

  print('Integer Constant : $myConstInt');
  print('Double Constant : $myConstDouble');
  print('String Constant : $myConstString');
}