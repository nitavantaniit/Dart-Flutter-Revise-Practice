// Program for Arithmetic Operators

void main()
{
  int a = 10;
  int b = 3;

  //Addition
  int sum = a + b;
  print("Sum: $sum");

  //Subtraction
  int difference = a - b;
  print("difference: $difference");

  //Multiplication
  int product = a * b;
  print("Product: $product");

  //Division
  double quotient = a / b;
  print("Quotient: $quotient");

  //Modulo
  int remainder = a % b;
  print("Remainder: $remainder");

  //Integer Division
  int integerQuotient = a ~/ b;
  print("Integer Quotient: $integerQuotient");

  //Unary Minus
  int negativeValue = -a;
  print("Negative Valueof a: $negativeValue");
}